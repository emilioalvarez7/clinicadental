    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        String obrasocial;
        int carnetobrasocial=0;
        float cuenta = 0;
        String titular;
        
        String errores = "";
        jScrollPane1.setVisible(true);
        try {
            carnetobrasocial = Integer.parseInt(this.jTextField2.getText());
        } catch (Exception ex) {
             errores+= "carnetobrasocial debe ser un nÃºmero.\n";
        }
        
        try {
            cuenta = Float.parseFloat(this.jTextField3.getText());
        } catch (Exception ex) {
             errores+= "Telefono debe tener menos de 10 digitos.\n";
        }
        
        obrasocial = this.txtObrasocial.getText();
        if (obrasocial.length()==0){
            errores += "Debe ingresar un nombre\n";
        }
        
        
        
        titular=this.jTextField4.getText();
        
        if (errores.length()>0){
            errortxt.setText(errores);
            return;
        }else{
            ObraSocial aux = new ObraSocial(obrasocial,carnetobrasocial,cuenta,titular);
            PDP.addPagos(aux);
            clean();
        }
    }                                        

